-- Production module: workstations/operations, PC rates, jobs + routing,
-- production entries, QC + rework, operation closures, transfer challans.
--
-- Design notes (deviations from the legacy Firestore-array model, see
-- com.niic.erp.production package-info for the full research writeup):
--   * Legacy job numbers were a single global counter (despite a stale code
--     comment claiming "per style") allocated via a non-atomic client-side
--     read-increment-write. We preserve the *global* numbering behavior
--     (business continuity) but make it atomic via a real DB sequence.
--   * Legacy transfer challan numbers reset per calendar year (TC-26-0001).
--     There's no portable "sequence that resets yearly" in standard SQL, so
--     we use a small counter table with pessimistic locking instead.
--   * Job requests and routing change requests are disposable staging data —
--     once approved they're copied into the real jobs/routing tables and
--     never read again in their original form. Duplicating the full
--     job/routing relational shape for that staging step isn't worth it, so
--     their colour/size/routing payload is stored as JSON text and parsed
--     only at review time.
--   * fg_item_id / variant_id / item_id columns reference the not-yet-built
--     Store module (fstore_items / fstore_variants). Left as plain bigint
--     with no FK constraint until that module exists.

create sequence job_number_seq start with 1 increment by 1;

create table styles (
    id         bigint generated by default as identity primary key,
    code       varchar(50)  not null unique,
    label      varchar(255) not null,
    active  boolean      not null default true,
    created_at timestamp    not null,
    updated_at timestamp    not null
);

